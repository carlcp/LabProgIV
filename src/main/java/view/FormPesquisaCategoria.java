    private void txtNomeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomeKeyReleased
        //caso queira pesquisar com enter
        //if (evt.getKeyCode() == KeyEvent.VK_ENTER){
            //recuperar o nome utilizado na pesquisa
            String nome = txtNome.getText();

            if (nome.isEmpty()){
                //exibir todos os registros da tabela
                preencherTabela(new CategoriaDAO().listar());
            }else{
                //pesquisar pelo nome
                preencherTabela(
                    new CategoriaDAO ().pesquisarPorNome(nome)
                );
            }

    }//GEN-LAST:event_txtNomeKeyReleased

    private void tabDadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabDadosMouseClicked

        //verificar se o usuario clicou 2x
        if (evt.getClickCount() == 2){

            //recuperar o ID da categoria selecionada
            int linha = tabDados.getSelectedRow();
            String id = tabDados.getValueAt(linha,0).toString();
            //abrir o formulario de cadastro e centralizar na tela
            FormCategoriaCadastro f = new FormCategoriaCadastro(id);
            Dimension d = this.getDesktopPane().getSize();
            this.getDesktopPane().add(f);
            f.setLocation( (d.width-f.getSize().width)/2,
                (d.height-f.getSize().height)/2);
            f.setVisible(true);

            //fechar o formulario de pesquisa
            this.dispose();

        }

    }//GEN-LAST:event_tabDadosMouseClicked


