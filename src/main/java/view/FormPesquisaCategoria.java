private void configurarForm(){
        this.setTitle("Pesquisa de Categoria");
        this.setResizable(false);
        this.setClosable(true);
        configurarTabela();
        preencherTabela(new CategoriaDAO().listar());
    }
    
    private void configurarTabela(){
        DefaultTableModel m = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        m.addColumn(("id"));
        m.addColumn(("nome"));
        m.addColumn(("Ativo"));
        tabDados.setModel(m);
    }
    
      private void preencherTabela(List<Categoria> lista) {

        if (lista != null) {
            if (lista.size() > 0) {
                configurarTabela();
                DefaultTableModel m = (DefaultTableModel)tabDados.getModel();
                for(Categoria obj : lista){
                    m.addRow(new Object[]
                        {
                            obj.getId(),
                            obj.getNome(),
                            obj.getFg_ativo() == 0 ? "Nao" : "sim"
                        }
                    );
                }
                tabDados.setModel(m);
            } else {
                configurarTabela();
                JOptionPane.showMessageDialog(null, "A tabela não contém dados.", "Loja",
                        JOptionPane.INFORMATION_MESSAGE
                );
            }
        } else {
            JOptionPane.showMessageDialog(null,"Ocorreu um erro.","Loja",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }
