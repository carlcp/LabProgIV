    private void tabDadosClienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabDadosClienMouseClicked

        //verificar se o usuário clicou 2x
        if (evt.getClickCount() == 2){

            //recuperar o ID da categoria selecionada
            int linha = tabDadosClien.getSelectedRow();
            String id = tabDadosClien.getValueAt(linha, 0).toString();

            //abrir o formulário de Produto e centralizar na tela
            FormCadastrarProduto f = new FormCadastrarProduto(id);
            Dimension d = this.getDesktopPane().getSize();
            this.getDesktopPane().add(f);
            f.setLocation( (d.width-f.getSize().width)/2, (d.height-f.getSize().height)/2);
            f.setVisible(true);

            //fechar o formulário de pesquisa
            this.dispose();
        }
    }//GEN-LAST:event_tabDadosClienMouseClicked


