    private void configurarForm() {
        this.setTitle("Pesquisa de Produto");
        this.setResizable(false);
        this.setClosable(true);
        configurarTabela();
        
        preencherTabela(new ClienteDAO().listar());
        
    }

    private void configurarTabela() {
        DefaultTableModel m = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        m.addColumn("Id");
        m.addColumn("Nome");
        m.addColumn("CPF");
        m.addColumn("DDD");
        m.addColumn("Numero");
        m.addColumn("Ativo");
        tabDadosClien.setModel(m);
    }

    private void preencherTabela(List<Cliente> lista) {

        if (lista != null) {
            if (lista.size() > 0) {
                configurarTabela();
                DefaultTableModel m = (DefaultTableModel)tabDadosClien.getModel();
                for(Cliente obj : lista){
                    m.addRow(new Object[]
                        {
                            obj.getId(),
                            obj.getNome(),
                             //exibir o nome da Categoria
                            new TelefoneDAO().pesquisarPorId(
                                Integer.toString(obj.getId())
                            ),
                            obj.getFg_ativo() == 0 ? "Não" : "Sim"
                        }
                    );
                }
                tabDadosClien.setModel(m);
            } else {
                configurarTabela();
                JOptionPane.showMessageDialog(null, "A tabela não contém dados.", "Produto",
                        JOptionPane.INFORMATION_MESSAGE
                );
            }
        } else {
            JOptionPane.showMessageDialog(null,"Ocorreu um erro.","Produto",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }
    
